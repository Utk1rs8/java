public class RelationalOperator {
    public static void main(String[] args) {
        int a=10;
        int b=20;
        // Equal to (==)
        System.out.println("a==b "+ (a==b));
        // Not Equal to (!=)
        System.out.println("a!=b "+ (a!=b));
        // Greater Than (>)
        System.out.println("a>b "+ (a>b));
        // Less Than (<)
        System.out.println("a<b "+ (a<b));
        // Greater than Equal to (>=)
        System.out.println("a>=b "+ (a>=b));
        // Less Than Equal to (<=)
        System.out.println("a<=b "+ (a<=b));
    }
    
}
